create index appointments_status_idx on public.appointments (status);
create index audit_logs_clinic_created_idx on public.audit_logs (clinic_id, created_at desc);

create or replace function public.current_clinic_id()
returns bigint
language sql
stable
security definer
set search_path = public
as $$
  select clinic_id
  from public.profiles
  where id = auth.uid()
$$;

create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
declare
  default_clinic_id bigint;
  requested_clinic_id bigint;
  requested_role text;
begin
  select id into default_clinic_id
  from public.clinics
  order by id
  limit 1;

  if default_clinic_id is null then
    insert into public.clinics (name, timezone)
    values ('Nodi Family Clinic', 'America/Bogota')
    returning id into default_clinic_id;
  end if;

  requested_clinic_id := case
    when coalesce(new.raw_user_meta_data ->> 'clinic_id', '') ~ '^[0-9]+$'
      then (new.raw_user_meta_data ->> 'clinic_id')::bigint
    else default_clinic_id
  end;

  requested_role := lower(coalesce(new.raw_user_meta_data ->> 'role', 'admin'));

  insert into public.profiles (id, clinic_id, email, full_name, role)
  values (
    new.id,
    requested_clinic_id,
    coalesce(new.email, ''),
    coalesce(new.raw_user_meta_data ->> 'full_name', split_part(coalesce(new.email, ''), '@', 1)),
    case when requested_role = 'staff' then 'staff'::public.app_role else 'admin'::public.app_role end
  )
  on conflict (id) do update
  set
    clinic_id = excluded.clinic_id,
    email = excluded.email,
    full_name = excluded.full_name,
    role = excluded.role,
    updated_at = timezone('utc', now());

  return new;
end;
$$;

create or replace function public.log_appointment_changes()
returns trigger
language plpgsql
security definer
set search_path = public
as $$
begin
  insert into public.audit_logs (
    clinic_id,
    actor_user_id,
    action,
    entity_type,
    entity_id,
    details
  )
  values (
    coalesce(new.clinic_id, old.clinic_id),
    auth.uid(),
    tg_op,
    'appointment',
    coalesce(new.id, old.id),
    jsonb_build_object(
      'before', to_jsonb(old),
      'after', to_jsonb(new)
    )
  );

  return coalesce(new, old);
end;
$$;

